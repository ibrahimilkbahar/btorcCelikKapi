package tr.com.mindworks.model;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

import org.springframework.data.annotation.Transient;

/**
 *
 * @author abraham
 */
@Entity
@Table(name = "t_product_case_type")

public class TProductCaseType implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "Id")
    private Integer id;
 
    @Column(name = "Name")
    private String name;
 
    @Column(name = "Code")
    private String code;
    
    @Column(name = "OrderBy")
    private Integer orderBy;
    
    
    public TProductCaseType() {
    }

    public TProductCaseType(Integer id) {
        this.id = id;
    }
    
    public TProductCaseType(int id, String name, String code) {
        this.id = id;
        this.name = name;
        this.code = code;
	}

	@Transient
	public static final TProductCaseType CASE_TYPE_HALFCASE = new TProductCaseType(1, "YarÄ±m Kasa", "HALFCASE");
    @Transient
	public static final TProductCaseType CASE_TYPE_FULLCASE = new TProductCaseType(2, "Tam Kasa", "FULLCASE");
    @Transient
	public static final TProductCaseType CASE_TYPE_PROFILECASE = new TProductCaseType(2, "Profil Kasa", "PROFILECASE");
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public Integer getOrderBy() {
        return orderBy;
    }

    public void setOrderBy(Integer orderBy) {
        this.orderBy = orderBy;
    }

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		TProductCaseType other = (TProductCaseType) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	

	

   
    
}
